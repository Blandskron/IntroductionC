#include <stdio.h>

int main() {
    // Declaración de variables
    int edad;           // Variable para almacenar la edad
    float altura;       // Variable para almacenar la altura
    char inicial = 'J'; // Variable para almacenar una letra

    // Inicialización de variables
    edad = 25;
    altura = 1.75;

    // Uso de las variables
    printf("Mi edad es %d años.\n", edad);
    printf("Mi altura es %.2f metros.\n", altura);
    printf("Mi nombre empieza con la letra %c.\n", inicial);

    return 0;
}
